package Variable;


public class VariableInJava {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println("a");
        System.out.println(a+b);
        String name = "Tony Stark";
        System.out.println(name);

        a=90;
        System.out.println(a);
        System.out.println(a+b);
        a=b;
        System.out.println(a);
    }
}
/*
 *           a,b,c ........... identifier
 *           class names and method names ..................identifier 
 */