package Variable;

import java.util.Scanner;

public class SumOfAna_B {
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a+b;
        System.out.println(sum);

        int product = a*b;
        System.out.println(product);
    }
}
